nome = "Felipe"
# integer
idade = 30
print("Meu nome é %s " % nome) 
print("Tenho %d anos" % idade)

palavra = "tomate"
# float
quantidade = 9.5
print("A palavra: %s " % palavra) 
print("Quantidade %f (float)" % quantidade)
print("Quantidade (string): " + str(quantidade) )

# concatenar
quantidade = 7.3
print("Esse número é o: " + str( quantidade ) ) 

# formatar
print("Esse número formatado: " + format( quantidade, '.2f' ) ) 
